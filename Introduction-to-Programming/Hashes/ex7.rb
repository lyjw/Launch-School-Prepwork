# Exercise 7
# Given the following code...

# x = "hi there"
# my_hash = {x: "some value"}
# my_hash2 = {x => "some value"}

# What's the difference between the two hashes that were created?

From the syntax used, we know that the key, x, of my_hash is a symbol. 
The key-value pair in my_hash2 was created using a hash rocket, =>, 
so the key, x, is a string.